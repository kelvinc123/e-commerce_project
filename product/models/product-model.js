const mongoose = require("mongoose");
const Schema = mongoose.Schema;

const ProductSchema = Schema({
    title: {
        type: String,
        required: true,
    },
    description: {
        type: String,
        required: true,
    },
    category: {
        type: String,
        required: true,
    },
    image: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    quantity: {
        type: Number,
        required: true,
    }
}, {collection: 'Product'});

module.exports = mongoose.model("Product", ProductSchema);
